package calc;

public class Calculatrice {

	public int addition(int a, int b) {
		int result;
		result = a + b;
		return result;
	}

	public int soustraction(int a, int b) {
		int result;
		result = a - b;
		return result;
	}

	public int multiplication(int a, int b) {
		int result;
		result = a * b;
		return result;
	}

	public int division(int a, int b) {
		int result;
		result = a / b;
		return result;
	}
}
